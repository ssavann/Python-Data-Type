#If I want "Round" numbers instead of floating
print(8 / 3)        #not rounded -> 2.666666666666
print(round(8 / 3))	#for round numbers -> 3


#chose how many decimals to round: two decimal numbers -> 2.67
print(round(8 / 3, 2))

#chose how many decimals to round: three decimal numbers -> 2.667
print(round(8 / 3, 3))


#To divide a number into an "integer" instead of a float -> 2
print(8//3)

#shorthand operation
score = 4 / 2
